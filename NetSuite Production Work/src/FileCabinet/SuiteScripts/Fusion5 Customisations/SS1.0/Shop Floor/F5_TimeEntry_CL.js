/**
 * The recordType (internal id) corresponds to the "Applied To" record in your script deployment. 
 * @appliedtorecord recordType
 *   
 * @returns {Boolean} True to continue save, false to abort save
 */
function clientSaveRecord(){
	
	var endTime = nlapiGetFieldValue("custrecord_f5_sfc_te_endtime");
	var startTime = nlapiGetFieldValue("custrecord_f5_sfc_te_starttime");
	nlapiLogExecution("DEBUG", "start time, end time", startTime+"***"+endTime);
	var duration = CalculateDeduction(startTime,endTime);
	if(duration>=0){
		var duration_min = duration/60;
		duration_min = Math.round(duration_min * 100)/100;
		var duration_hr = duration/3600;
		duration_hr = Math.round(duration_hr * 100)/100;

		nlapiLogExecution("DEBUG", "min, hr", duration_min+"***"+duration_hr);
		nlapiSetFieldValue("custrecord_f5_sfc_te_durationinmints", duration_min, false);
		nlapiSetFieldValue("custrecord_f5_sfc_te_durationinhours", duration_hr, false);
	}
	else{
		alert("Start Date and End Date are not in the same day.");
		return false;
	}
    return true;
}

